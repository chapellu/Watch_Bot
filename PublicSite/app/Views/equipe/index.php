	<div class="content-row">
		<p class="intro" >
		L'Apprentissage par Problèmes et par Projet (APP) est réparti sur quatre semestres. 
		Chaque semestre a une importance différente sur l'avancement du projet. 
		Le semestre 6 se concentre sur l'étude du thème, la formation sur la prise en main des 
		différents robots utilisés et leurs logiciels de programation.
		Le semestre 7 porte sur la rédaction du cahier des charges et le dossier de conception.
		La réalisation du projet se fait essentiellement sur le semestre 8 et peut se finir sur
		le semestre 9, qui s'axe sur la communication autour du projet. </p>
	
		<p class="repartition-roles" >
		Afin de mener à bien notre projet et de répondre aux attentes de l'APP nous nous 
		sommes répartis différents rôles. Ces rôles sont animateur, chef de projet, secrétaire 
		et responsable matériel. Ils sont affectés à une personne différente à chaque séance suivant 
		un planning. 
		</p>
		
		<ul>
			<li>Le chef de projet est responsable de la communication entre le groupe et les enseignants
			ainsi que la surveillance de l'avancement du projet. Il veille à la cohésion au sein du
			groupe de façon à ce que le projet avance dans les meilleurs conditions.
			Néanmoins, celui-ci est redistribué au bout de quatre séances à un autre étudiant, afin que 
			chacun puisse expérimenter les différents rôles.
			</li>
			<li>L'animateur est responsable de la dynamique de la séance ainsi que de la bonne conduite
			des débats et des votes. Il veille à ce que chacun puisse donner son avis.
			</li>
			<li>Le secrétaire quand à lui s'occupe du compte-rendu de séance, son rôle consiste à
			synthétiser ce qui a été dit et fait pendant la séance pour faciliter les transitions
			d'une séance à l'autre.
			</li>
			<li>Le responsable matériel installe les tapis de test, sort les robots, les met en charge 
			si besoin et veille à ce que les consignes de sécurité soient respectées.
			À la différence du semestre 6, où les rôles nous étaient imposés, ce rôle a été supprimé. 
			En effet, nous avons trouvé plus judicieux que ce soit l'ensemble du groupe qui puisse 
			être en charge du matériel.
			</li>
		</ul>
	
		<p class="repartition-poles">
		Pour pouvoir avancer facilement dans l'étude et la réalisation du projet, nous nous 
		sommes répartis en différents pôles de travail, constitués de deux à trois étudiants, qui sont:
		</p>
		<ul>
			<li>Pôle Capteurs</li>
			<li>Pôle Cartographie</li>
			<li>Pôle Communication</li>
			<li>Pôle Navigation</li>
		</ul>
		
		<p class="organisation_seances">
		Afin d’être le plus efficace possible lors de nos séances de projet, nous avons dû 
		nous habituer à utiliser certains réflexes organisationnels.
		Au début de chaque séance, nous prenions 10 à 15 minutes pour discuter des objectifs 
		que nous souhaitions nous fixer au cours de celle-ci dans les différents pôles cités 
		précédemment.
		Nous faisions des réunions très fréquemment au cours d’une séance afin de mettre 
		chaque groupe au courant des avancées ou lorsqu’il était nécessaire de prendre des 
		décisions qui auraient une influence sur le reste du projet.
		Chaque fin de séance était réservée pour effectuer un bilan de chaque pôle.
		Ce bilan étant reporté au travers des fiches de fin de séance, rédigées par le 
		secrétaire.
		</p>
